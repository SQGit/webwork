                <h5>How it works:</h5>
                <p>The Ontario’s property assessment and taxation system is comprised of five components:</p>
                <ul>
                <li>The Provincial Government which is responsible for passing legislation and setting property tax policies and education tax rates.</li>
                <li>The Assessment Act (“Act”) which is the provincial legislation that governs how property tax is assessed in Ontario.</li>
                <li>MPAC (The Municipal Property Assessment Corporation) which establishes the classifications and current value assessments and for all Ontario Properties.</li>
                <li>·Municipalities which establishes the municipal tax rates, the revenue requirements and the collection of taxes.</li>
                <li>The Assessment Review Board (“ARB”) is a tribunal that hears tax assessment appeals.</li>
                </ul>
                <p>Commercial and Industrial Properties (Including Others</p>
                <p>The deadline to dispute your Commercial / Industrial Property Taxes with the ARB is March 31st. If you Fail to appeal within that time frame, you will be prohibited from disputing your property taxes. Get Legal Help can assist you with your Request for Reconsideration (“RfR”) once you have received you Notice. This may result in a correction prior to the years tax bills being distributed. If you are not in agreement with the RfR decision, you may file an appeal with the ARB within 90 days.</p>
                <p>Residential, Farm or Managed Forest Properties</p>
                <p>If your property is classified as residential, farm or managed forest properties, you will be required to execute the RfP before you are eligible to file an appeal with the ARB. March 31, 2013 is the deadline to complete and file an RfP with MPAC . Once a decision has been rendered on your request for reconsideration, if you are still not in agreement Get Legal Help will assist you in meeting the 90 day requirement to file an appeal with the Assessment Review Board.</p>

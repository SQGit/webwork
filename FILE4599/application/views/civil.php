            <p>The Ontario Small Claims Court was designed to be an informal way to settle disputes monetary or property claims up to $25,000. Get Legal Help can assist you in resolving disputes in simple or complex matters.</p>

            <h4>Small Claim Court Procedures in Ontario</h4>
            <h5>How to Commence the Claim</h5>
            <p>The individual commencing the claim is called the “Plaintiff”. To begin the process,   a “Plaintiff’s Claim” form is completed and the Ontario Court of Justice is courts is paid the required fee.  The form will indicate the reasons you have brought forth your claim and the monetary value you wish to achieve.</p>
            <h5>Serving the Defendant</h5>
            <p>Upon filing with the courts, Get Legal Help will personally provide a copy of your claim to the defendant within six months of the claim being issued by the courts.</p>
            <h5>Responding to the Claim (Defendant)</h5>
            <p>The “Defendant” is the individual being sued.  Within 20 days of the Claim being received, the Defendant must file with the courts a “Defence” and if applicable a Counterclaim, against the Plaintiff. If the Defendant fails to dispute all or part of the claim, with the courts, a judgement may be entered against him/her by way of a Motion brought forth by the Plaintiff. It is important to note that if a Default judgement is entered against the Defendant, he/she may not receive any further notification.</p>
            <h5>The Settlement Conference</h5>
            <p>Within 90 days of the Defendant filing a response to the claim that has been received, a mandatory settlement conference is held before a judge. The purpose of the Settlement Conference is to make attempts for both parties to settle the claim without having to proceed to trial.</p>
            <p>Settlement Conferences are also used to obtain full disclosure from the involved parties in respects to evidence. It is also an opportunity to pin point the highlights of the claim, and assist in trial preparation.</p>
            <h5>Going to Trial</h5>
            <p>When acclaim proceeds to trial, both parties (Plaintiff and Defendant) will be given an opportunity to present their position. This is done through the witness statements and the supporting evidence filed. Each party will also be given the opportunity to cross-examine (ask questions) of the opposing side. If either party fails to attend the trial date, the trial will proceed in your absence. Once all the evidence has been heard, the presiding Judge will take all the information into consideration and render a decision.</p>
            <h4>Appealing a Small Claims Court Judgment</h4>
            <p>A Small Claim Court Judgement can be appealed as long as the claim exceeds $500.</p>          
